function sumarHastaN(n) {
  var acumulador=0;

  for (var i = 1; i <= n ; i++){
    acumulador= acumulador + i;


  }
  return acumulador;

  // La función recibe un número "n" por argumento.
  // Devuelve la suma de todos los números desde 1 hasta n.
  // Tu código:
}

module.exports = sumarHastaN;
